class Child {
  String type = " ";
  String text = "";
  int x = 0;
  int y = 0;
  String color = "";
  int fontSize = 0;
  bool bold = false;
  bool italic = false;
  String src = "";
  int height = 0;
  int width = 0;

  String getType() {
    return type;
  }

  void setType(String type) {
    this.type = type;
  }

  String getText() {
    return text;
  }

  void setText(String text) {
    this.text = text;
  }

  int getX() {
    return x;
  }

  void setX(int x) {
    this.x = x;
  }

  int getY() {
    return y;
  }

  void setY(int y) {
    this.y = y;
  }

  String getColor() {
    return color;
  }

  void setColor(String color) {
    this.color = color;
  }

  int getFontSize() {
    return fontSize;
  }

  void setFontSize(int fontSize) {
    this.fontSize = fontSize;
  }

  bool getBold() {
    return bold;
  }

  void setBold(bool bold) {
    this.bold = bold;
  }

  bool getItalic() {
    return italic;
  }

  void setItalic(bool italic) {
    this.italic = italic;
  }

  String getSrc() {
    return src;
  }

  void setSrc(String src) {
    this.src = src;
  }

  int getHeight() {
    return height;
  }

  void setHeight(int height) {
    this.height = height;
  }

  int getWidth() {
    return width;
  }

  void setWidth(int width) {
    this.width = width;
  }
}
